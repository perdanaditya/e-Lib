    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        penerbitList = new ArrayList<Penerbit>();
        String searchCol = searchColumnCb.getSelectedItem().toString();
        String searchParam = searchValue.getText();
        System.out.println("param " + searchParam);
        System.out.println("search col " + searchCol);
        if (!searchParam.equals("")) {
            penerbitList = penerbitController.search(masterPenerbitList, searchParam, searchCol);
        } else {
            penerbitList = masterPenerbitList;
        }
        System.out.println("SIZE MASTER " + masterPenerbitList.size());
        System.out.println("SIZE LIST " + penerbitList.size());
        setTableValue();
    }//GEN-LAST:event_searchBtnActionPerformed

    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String nama = namaPenerbit.getText();
        String alamat = alamatPenerbit.getText();
        String kota = kotaPenerbit.getText();
        String telpon = teleponPenerbit.getText();
        if (!nama.equals("") && !alamat.equals("") && !kota.equals("") && !telpon.equals("")) {
            penerbitModel.setNamaPenerbit(nama);
            penerbitModel.setAlamatPenerbit(alamat);
            penerbitModel.setKotaPenerbit(kota);
            penerbitModel.setTelponPenerbit(telpon);
            penerbitModel.setActive(true);
            penerbitModel.setUserInput(DataLibrary.getInstance().getUser().getUsername());

            penerbitController.setPenerbitModel(penerbitModel);
            penerbitController.setEditMode(editMode);
            String message = penerbitController.save();
            if (!message.equals("success")) {
                JOptionPane.showMessageDialog(null, (editMode ? "Update" : "Submit") + " data gagal", "Submit Data Error", JOptionPane.ERROR_MESSAGE);
            }
            resetAllFieldInput();
            reInitValiables();

            //refresh table
            masterPenerbitList = penerbitController.fetchData(masterPenerbitList, true);
            penerbitList = masterPenerbitList;
            setTableValue();
            if(inputBukuMode){
                InputBuku inputBuku = InputBuku.getInstance(false);
                inputBuku.setPenerbitItem(false);
                inputBuku.penerbitCmb.setSelectedIndex(masterPenerbitList.size()-1);
                inputBukuMode = false;
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "One or more field is empty, Cannot insert data", "INPUT-ERROR", 0);
        }
    }//GEN-LAST:event_submitActionPerformed

