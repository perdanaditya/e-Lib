    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if (editButton.isSelected()) {
            editButton.setText("Cancel Edit");
            editMode = true;
            penerbitModel = penerbitList.get(tableList.getSelectedRow());

            namaPenerbit.setText(penerbitModel.getNamaPenerbit());
            alamatPenerbit.setText(penerbitModel.getAlamatPenerbit());
            kotaPenerbit.setText(penerbitModel.getKotaPenerbit());
            teleponPenerbit.setText(penerbitModel.getTelponPenerbit());
        } else {
            editButton.setText("Edit Record");
            editMode = false;
            penerbitModel = new Penerbit();
            tableList.clearSelection();
            resetAllFieldInput();
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Are you sure want to delete selected record?", "Delete Record",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            penerbitModel = penerbitList.get(tableList.getSelectedRow());
            penerbitModel.setActive(false);
            penerbitModel.setUserInput(DataLibrary.getInstance().getUser().getUsername());
            editMode = true;
            penerbitController.setPenerbitModel(penerbitModel);
            penerbitController.setEditMode(editMode);
            penerbitController.save();

            //reset table
            tableList.clearSelection();
            penerbitList = penerbitController.fetchData(masterPenerbitList, true);
            setTableValue();
            resetAllFieldInput();
            reInitValiables();
        } else {
            tableList.clearSelection();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


